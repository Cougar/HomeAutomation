#include <mcu.h>


/*-----------------------------------------------------------------------------
 * Prerequisites
 *---------------------------------------------------------------------------*/
#ifndef MCU
	#error MCU Not defined in mcu_cfg.h
#endif


/*-----------------------------------------------------------------------------
 * Public Functions
 *---------------------------------------------------------------------------*/

/**
 * Enable interrupts.
 */
void Mcu_EnableIRQ() {
	#if MCU == ATMEGA8
		sei();
	#endif
}

/**
 * Disable interrupts.
 */
void Mcu_DisableIRQ() {
	#if MCU == ATMEGA8
		cli();
	#endif
}
